    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(!jtfMaTh.getText().isEmpty()&&!jtfTenTh.getText().isEmpty()&&checkMaTh(jtfMaTh.getText())){
            lbMaTh.setText("");
            lbTenTh.setText("");
            thDto = new DTO_ThuongHieu(jtfMaTh.getText(), jtfTenTh.getText(), 1);
            if(thBus.addThuongHieu(thDto)){
                JOptionPane.showMessageDialog(null, "Thêm thành công!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                jtfMaTh.setText("");
                jtfTenTh.setText("");
                ql.setDateToTable();
            }
            else
                JOptionPane.showMessageDialog(null, "Thêm thất bại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
         else if(jtfMaTh.getText().isEmpty()&&jtfTenTh.getText().isEmpty()){
            lbMaTh.setText("không được để trống!");
            lbTenTh.setText("không được để trống!");
        }
        else if(jtfMaTh.getText().isEmpty()){
            lbTenTh.setText("");
            lbMaTh.setText("không được để trống!");
        }
        else if(jtfTenTh.getText().isEmpty()){
            lbMaTh.setText("");
            lbTenTh.setText("không được để trống!");
        }
        else if(!checkMaTh(jtfMaTh.getText())){
             lbTenTh.setText("");
             lbMaTh.setText("Mã bị trùng!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private boolean checkMaTh(String maTh){
        for (DTO_ThuongHieu th: thBus.getList()){
            if(maTh.equalsIgnoreCase(th.getMATH())){
                return false;
            }
        }
        return true ;
    }
